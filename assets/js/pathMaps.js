let markers = [];

function initMap() { //main map view - code insired by the User-Centric module from Code Institute
  var map = new google.maps.Map(document.getElementById("map"));

    // markers
    if (window.location.pathname.indexOf("/dudley1.html") != -1){
        markers = dudley1;
    } else if (window.location.pathname.indexOf("/dudley2.html") != -1){
        markers = dudley2;
    } else if (window.location.pathname.indexOf("/stourbridge-path.html") != -1){
        markers = stourbridge;
    }

  var mapOptions = {
    center: new google.maps.LatLng(
      markers[0].lat,
      markers[0].lng
    ),
    zoom: 11,
    mapTypeId: google.maps.MapTypeId.ROADMAP,
  };

  var map = new google.maps.Map(document.getElementById("map"), mapOptions);
  //InfoWindow.
  var infoWindow = new google.maps.InfoWindow();
  for (var i = 0; i < markers.length; i++) {
    var data = markers[i];
    var myLatlng = new google.maps.LatLng(data.lat, data.lng);
    var marker = new google.maps.Marker({
      position: myLatlng,
      map: map,
      title: data.title,
    });

    //click event to the marker.
    (function (marker, data) {
      google.maps.event.addListener(marker, "click", function (e) {
        infoWindow.setContent(
           "<div style = 'width:100%;height:100%;'>" + "<div style = 'font-weight:bold;'>" + data.title + "</div>" + data.description + "</div>" 
        );
        infoWindow.open(map, marker);
      });
    })(marker, data);
  }
}
